let day = 9


// matching in switch using === -> the same type and value
switch(day) {
    case 0:
        console.log(`day is ${day} -> saturday`)
        break;
    
    case 1:
        console.log(`day is ${day} -> sunday`)
        break;

    case 2:
        console.log(`day is ${day} -> monday`)
        break;

    case 3:
        console.log(`day is ${day} -> tusday`)
        break;

    case 4:
        console.log(`day is ${day} -> wendsday`)
        break;

    case 5:
        console.log(`day is ${day} -> thursday`)
        break;
    
    case 6:
        console.log(`day is ${day} -> friday`)
        break;
    
    default:
        console.log("wrong number value")
        break;
}

